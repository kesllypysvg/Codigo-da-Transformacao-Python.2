def saudacao(nome):
    """
    Imprime uma saudação personalizada com o nome fornecido.

    Args:
        nome (str): O nome da pessoa a ser cumprimentada.
    """
    print(f"Olá, {nome}! Bem-vindo(a)!")

# Exemplos de uso da função:
saudacao("Maria")
saudacao("Carlos")
saudacao("Ana")
